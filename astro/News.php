      <h2>Selected Publicity</h2>

<?include("menu.html")?> <!-- with php -->

      <p>
	It has been fun to have some of my work catch a tiny bit of
	limelight from the broader community.  Here are a few projects
	that drew some notice:
      </p>
      
      <p>
&bull; <a href="http://news.yahoo.com/model---could---help---d---potenGally---habitable---alien---planets---184631265.html" target="_blank"><b>A statistical method</b></a> of quantifying the minimum atmospheric height of an exoplanet, that I worked on together with David Kipping and Dimitar Sasselov of CfA, was featured on Yahoo News.</p>

      <p align="right"><a href="http://www.cfa.harvard.edu/news/2011/pr201121.html" target="_blank"><img src="imgs/lores.jpg" height="100pix" style="float: right" border="1pix"></a></p>

      <p>
&bull; The <a href="http://adsabs.harvard.edu/abs/2013ApJ...774...11K" target="_blank"><b>discovery</b></a> of a low-mass, directly-imaged exoplanet around GJ 504 led to a <a href="http://www.nasa.gov/content/goddard/astronomers-image-lowest-mass-exoplanet-around-a-sun-like-star/index.html#.UhUVOWTwLm0" target="_blank"><b>press release</b></a> and attention from <a href="http://www.space.com/22192-gas-giant-planet-directly-imaged.html" target="_blank"><b>Space.com</b></a>.
      </p>

      <p>
&bull; In summer of 2011, a collaboration with David Kipping of CfA, dubbed <a href="http://adsabs.harvard.edu/abs/2011MNRAS.417L..88K" target="_blank"><b>"The Darkest World" (Kipping & Spiegel 2011 MNRAS)</b></a>, was briefly the top news story on <a href="http://news.yahoo.com/coal-black-alien-planet-darkest-ever-seen-220601419.html" target="_blank"><b>Yahoo News</b></a> and was also written up on <a href="http://www.time.com/time/health/article/0,8599,2089304,00.html" target="_blank"><b>Time.com</b></a>, among other sites.  This also one of many topics covered in a <a href="WAMC/TBOOK_1125_0.mp3" target="_blank"><b>radio interview</b></a> with me about exoplanets on NPR affiliate WAMC.
      </p>

      <p>
&bull; <a href="http://adsabs.harvard.edu/abs/2012PNAS..109..395S" target="_blank"><b>A Bayesian analysis</b></a> of the astrobiological implications of the early emergence of life on Earth, in collaboration with Ed Turner (<a href="http://adsabs.harvard.edu/abs/2012PNAS..109..395S" target="_blank"><b>Spiegel & Turner 2012 PNAS</b></a>), led to a <a href="http://www.princeton.edu/main/news/archive/S33/52/89I01/" target="_blank"><b>press release</b></a> and attracted the attention of <a href="http://www.huffingtonpost.com/seth-shostak/life-in-space-seti_b_914963.html" target="_blank"><b>The Huffington Post</b></a>, MIT's magazine <a href="http://www.technologyreview.com/view/424795/probability-of-et-life-arbitrarily-small-say-astrobiologists/" target="_blank"><b>Technology Review</b></a>, <a href="http://www.nj.com/mercer/index.ssf/2012/04/princeton_scientists_say_truth.html" target="_blank"><b>The Trenton Times</b></a>, and more.  (Note that the Technology Review piece mischaracterizes our work somewhat.)
      </p>

