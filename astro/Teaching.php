      <p>
	I'm a dedicated and passionate teacher across a broad range of levels, from advising and mentoring young researchers to helping inmates learn skills that will help them integrate into society post-release.  I have taught in a traditional lecture style and in a lecture-free, inquiry-based format (e.g., at <a href="http://www.questu.ca" target="_blank">Quest University</a> &mdash; see below, and in the astronomy lab classes I taught in graduate school).
      </p>
<p>&bull; I have supervised several undergraduate students' research, leading to four research papers that have been published in peer-reviewed journals.</p>
<p>&bull; For the past three summers, I have co-organized the highly successful <a href="http://www.princeton.edu/astro/undergraduate/usrp/" target="_blank">Undergraduate Summer Research Program</a> at Princeton's Astrophysics Department. I interviewed candidates, matched students with research mentors, taught a research class that focused on skills such as Bayesian statistics and "how to give a research talk," and supervised research projects.</p>
<p>&bull; In the fall of 2011, I spent a very enjoyable couple weeks designing and teaching an intensive course in astrobiology at <a href="http://www.questu.ca" target="_blank">Quest University</a> in British Columbia, Canada.</br>
<p>&bull; For the past 5 years, I have taught in the <a href="http://www.princeton.edu/pace/home/programs/prisonereducation/" target="_blank">Prison Teaching Initiative</a> at Princeton, a collaboration between Princeton University, Mercer County Community College, and the New Jersey Department of Corrections, that brings college courses to prisons.</p>
<p>&bull; In graduate school, I was named the Astronomy Department's "Outstanding Teaching Assistant" in 2005 and was the Head Teaching Assistant in 2004.  For 8 semesters, I designed, taught, and graded a laboratory astronomy class.</p>
<p>&bull; Also, in graduate school, I organized Family Astro days and Public Observing nights, occasionally attracting as many as ~1000 attendants (for Mars-viewing events around the time of those ridiculous <a href="http://www.slate.com/blogs/bad_astronomy/2013/08/22/mars_as_big_as_the_moon_no_just_no.html" target="_blank"><i>Mars will look as big as the Moon!</i></a> internet "hoaxes").  In addition, I developed a pilot program to bring astronomy to elementary school classrooms, in partnership with the Anderson School in Manhattan (2007-2008).</p>
